// Scans app/ for page.tsx (and page.ts, page.mdx, etc.) and emits lib/dev-kiosk-routes.generated.ts
// Run: node scripts/generate-dev-kiosk-routes.mjs
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const appDir = path.join(root, "app");
const outFile = path.join(root, "lib", "dev-kiosk-routes.generated.ts");

const PAGE_RE = /^page\.(tsx|ts|jsx|js|mdx)$/;

/** @param {string} dir @param {string[]} segments */
function walk(dir, segments) {
  /** @type {string[]} */
  const routes = [];
  let entries;
  try {
    entries = fs.readdirSync(dir, { withFileTypes: true });
  } catch {
    return routes;
  }
  for (const ent of entries) {
    const name = ent.name;
    if (name.startsWith("_")) continue;
    const full = path.join(dir, name);
    if (ent.isDirectory()) {
      const isGroup = name.startsWith("(") && name.endsWith(")");
      const nextSegs = isGroup ? segments : [...segments, name];
      routes.push(...walk(full, nextSegs));
    } else if (PAGE_RE.test(name)) {
      const pathname =
        segments.length === 0 ? "/" : `/${segments.join("/")}`;
      routes.push(pathname);
    }
  }
  return routes;
}

const raw = walk(appDir, []);
const EXCLUDE = new Set(["/dev-kiosk"]);
const unique = [...new Set(raw)]
  .filter((r) => !EXCLUDE.has(r))
  .sort((a, b) => a.localeCompare(b));

const lines = unique.map((r) => `  ${JSON.stringify(r)},`).join("\n");
const body = `// Generated by scripts/generate-dev-kiosk-routes.mjs — do not edit by hand.

export const DEV_KIOSK_APP_ROUTES = [
${lines}
] as const;

export type DevKioskAppRoute = (typeof DEV_KIOSK_APP_ROUTES)[number];
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, body, "utf8");
console.log(`Wrote ${outFile} (${unique.length} routes)`);
